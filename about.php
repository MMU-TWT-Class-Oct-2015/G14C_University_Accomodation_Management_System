<h1 style="text-align:center;">CHANCELLOR OF MU</h1>
<p> </p>
<p style="text-align: center;"><strong>TUN DATO' SERI ZAKI TUN AZMI</strong></p>
<p style="text-align: center;"><strong>Date of Appointment: 20 June 2012 </strong></p>
<p align="center" style="text-align: center;"><strong> <img   src="Images/mmuchancellor.jpg" /></strong></p>
<p style="text-align: justify;">The Honourable Zaki Tun Azmi was born on September 12, 1945 in Alor Setar, Kedah. He had his early education at Sultan Abdul Hamid College, Alor Setar, Anderson School, Ipoh and English College, Johor Bahru. He then read law in the Lincoln's Inn, London and obtained a Barrister-at-Law in 1969.</p>
<p style="text-align: justify;">Mr. Zaki Tun Azmi came from a family of lawyers. His father was the first in the line of Azmi lawyers. His deceased brother was the next which was followed by him. His second son and daughter-in-law are lawyers too. So is his wife who happens to be a first class student from the International Islamic University Malaysia but now a housewife. His father was the second Malay Chief Justice of Malaysia, then known as the Lord President (1968 to 1974).</p>
<p style="text-align: justify;">After having been in practice with an established law firm he was invited to be elevated to the bench as a Federal Judge. Prior to being in practice, he was in the Malaysian Judicial and Legal Service for 15 years ,joining as a Magistrate then later transferred to the Attorney Generals Chambers where he held several positions until he resigned to go into practice. While in practice he also sat on several public listed companies as a director as well as chairman or member of Board Audit Committees. Amongst which he participated were PETRONAS (the national oil company, which today has spread it wings throughout the world), Malaysia Airport Berhad and SP Setia. Perhaps it was this management experience that the CJ brought into the Judiciary to make it what it is today.</p>
<p style="text-align: justify;">After holding office as a Federal Judge for a few months he was appointed as the President of Court of Appeal, the second highest judicial office. On 18th October 2008, he was appointed to be the 12th Chief Justice of Malaysia. With the establishment of the Judicial Appointment Commission, he became the first Chairman since 16th February 2009.</p>
<p style="text-align: justify;">Tun Zaki is known for his success in getting rid of age old backlog problems faced by the Malaysian courts and ensuring more than 90 percent of currently registered cases to be disposed of within nine months of registration. This is the fastest in any common law countries. He did within less than three years during his term of office as the 11th Chief Justice of Malaysia.</p>
